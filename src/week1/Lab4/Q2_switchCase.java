package week1.Lab4;

public class Q2_switchCase {

	public static void main(String[] args) {
		char alpha = 'K';
		
		
		switch(alpha)
		{
		case 'a':
			System.out.println("vowel");
			break;
			
		case 'e':
			System.out.println("vowel");
			break;
			
		case 'd':
			System.out.println("consonant");
			break;
			
		default:
			System.out.println("incorrect");			
		}
	}

}
