package week1.Lab1;

public class Q1_AreaParimeterRectangle {

	public static void main(String[] args) {
		double area;
		double par;
		double length = 10;
		double breadth = 20;
		
		area = length * breadth;
		par = 2*(length + breadth);
		System.out.println("Area of a rectangle is: " + area);
		System.out.println("Parimeter of a rectangle is: " + par);

	}

}
